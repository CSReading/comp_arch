md"""
銀行丸めとは5に対して, 偶数方向に丸める処理を指す. つまり, 1.5は最も近い偶数である２に丸め, 4.5の最も近い偶数4に丸める操作のことを言う. ちなみに, Juliaの`round`関数はデフォルトで銀行丸めを採用している.
