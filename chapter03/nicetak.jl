	xs = rand(0.0:0.1:5.0, 100)
	s₁ = sum(xs)
	s₂ = 0
	s₃ = 0
	for x ∈ xs
		s₂ += round(x) #銀行丸め
		s₃ += round(x, RoundNearestTiesUp) # 四捨五入
	end
	PlutoUI.Print("Float: $s₁\nBanker: $s₂\n4D5U: $s₃")
end

