# ╔═╡ 18683827-af64-4b57-a571-d7d4425ada5e
md"""
簡単化のために, 4ビット(-7~7)の整数のみを考える.
"""

# ╔═╡ 567b2d93-0009-407a-b9b1-db95d9c7c8cb
let
	function sign(n::Int; num_bit = 4)
		if n ≥ 0
			return lpad(string(n, base=2), num_bit, "0")
		else
			return "1" * lpad(string(n, base=2)[begin+1:end], num_bit - 1, "0")
		end
	end
	
	function comp1(n::Int; num_bit = 4)
		if n ≥ 0
			return lpad(string(n, base=2), num_bit, "0")
		else
			return string(~UInt(-n), base = 2)[end - (num_bit - 1):end]
		end
	end
	
	function comp2(n::Int; num_bit = 4)
		if n ≥ 0
			return lpad(string(n, base=2), num_bit, "0")
		else
			return string(~UInt(-n-1), base = 2)[end - (num_bit - 1):end]
		end
	end
	
	DataFrame(
		n = -7:7, 
		comp1 = comp1.(-7:7), 
		comp2 = comp2.(-7:7), 
		sign = sign.(-7:7)
	)
end

